let fahrenheit = 50;
// calculate celsius and store in celsius variable
// print the value
let celsius = (fahrenheit - 32) * 5/9;
console.log(celsius +'c');
// calculate kelvin and store in kelvin variable
// print the value
let kelvin = (fahrenheit - 32) * 5/9 + 273.15;
console.log(kelvin +'k');